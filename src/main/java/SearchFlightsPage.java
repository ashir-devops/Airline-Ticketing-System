    private void btnBookSelectedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookSelectedActionPerformed
        int selectedRow = tblSearchResults.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a flight to book.");
            return;
        }

        String flightID = tblSearchResults.getValueAt(selectedRow, 0).toString();
        AirlineSystem.Flight flight = AirlineSystem.FlightHandler.getFlightByID(flightID);
        if (flight == null) {
            JOptionPane.showMessageDialog(this, "Selected flight not found.");
            return;
        }

        new BookingPage(flight).setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnBookSelectedActionPerformed

