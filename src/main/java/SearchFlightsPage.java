    private void btnSearchNowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchNowActionPerformed
        String src = txtSearchSource.getText().trim();
        String dst = txtSearchDestination.getText().trim();
        String date = txtSearchDate.getText().trim();

        ArrayList<AirlineSystem.Flight> flights = new ArrayList<>(AirlineSystem.FlightHandler.getAllFlights());
        DefaultTableModel model = (DefaultTableModel) tblSearchResults.getModel();
        model.setRowCount(0);

        for (AirlineSystem.Flight f : flights) {
            boolean matches = f.getSource().equalsIgnoreCase(src) &&
                    f.getDestination().equalsIgnoreCase(dst) &&
                    f.getDate().equals(date);
            if (matches) {
                model.addRow(new Object[]{
                        f.getFlightID(), f.getSource(), f.getDestination(),
                        f.getDate(), f.getTime(), f.getBaseFare(),
                        f.getFirstSeats(), f.getBusinessSeats(), f.getEconomySeats()
                });
            }
        }

        if (model.getRowCount() == 0) {
            scrollSearchResults.setPreferredSize(new Dimension(900, 150));
            JOptionPane.showMessageDialog(this, "No flights found for the selected criteria.");
        } else {
            int height = Math.min(model.getRowCount() * 25 + 25, 300);
            scrollSearchResults.setPreferredSize(new Dimension(900, height));
            pack();
        }
    }//GEN-LAST:event_btnSearchNowActionPerformed

