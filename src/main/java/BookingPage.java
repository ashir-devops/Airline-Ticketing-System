    private void updatePriceLabel() {
        String seatClass = (String) cmbClass.getSelectedItem();
        if (seatClass != null) {
            double price = 0;

            if (flight != null) {
                price = flight.getBaseFare();
                switch (seatClass) {
                    case "First": price *= 2; break;
                    case "Business": price *= 1.5; break;
                }
            } else {
                // Default prices if no flight is selected
                switch (seatClass) {
                    case "First": price = 200; break;       // example base price
                    case "Business": price = 150; break;
                    case "Economy": price = 100; break;
                }
            }

            lblPrice.setText(String.format("%.2f", price));
        }
    }


