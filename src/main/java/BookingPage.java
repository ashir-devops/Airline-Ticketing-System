    private void btnConfirmBookingActionPerformed(java.awt.event.ActionEvent evt) {
        if (flight == null) {
            JOptionPane.showMessageDialog(this, "No flight selected. Cannot confirm booking.");
            return;
        }

        String name = txtPassengerName.getText().trim();
        String cnic = txtPassengerCNIC.getText().trim();
        String contact = txtPassengerContact.getText().trim();
        String seatClass = (String) cmbClass.getSelectedItem();

        if (name.isEmpty() || cnic.isEmpty() || contact.isEmpty() || seatClass == null) {
            JOptionPane.showMessageDialog(this, "Please fill all fields and select a class.");
            return;
        }

        boolean booked = false;
        switch (seatClass) {
            case "First": if (flight.getFirstSeats() > 0) { flight.setFirstSeats(flight.getFirstSeats()-1); booked = true;} break;
            case "Business": if (flight.getBusinessSeats() > 0) { flight.setBusinessSeats(flight.getBusinessSeats()-1); booked = true;} break;
            case "Economy": if (flight.getEconomySeats() > 0) { flight.setEconomySeats(flight.getEconomySeats()-1); booked = true;} break;
        }

        if (!booked) {
            JOptionPane.showMessageDialog(this, seatClass + " class is full.");
            return;
        }
        List<AirlineSystem.Flight> flights = AirlineSystem.FlightHandler.getAllFlights();
        for (int i = 0; i < flights.size(); i++) {
            if (flights.get(i).getFlightID().equalsIgnoreCase(flight.getFlightID())) {
                flights.set(i, flight);
                break;
            }
        }
        AirlineSystem.FlightHandler.updateFlights(flights);

        String ticketID = "TKT" + System.currentTimeMillis();
        String seatNo = seatClass.charAt(0) + "-" + (int)(Math.random() * 40 + 1);
        String price = lblPrice.getText();
        String passengerID = "PAX" + System.currentTimeMillis();
        String[] passengerData = { passengerID, name, "0", "N/A", contact };
        DataManager.appendPassenger(passengerData);


        // Open Payment Page
        String[] ticketData = {
                ticketID,
                txtPassengerName.getText().trim(),  // Passenger Name
                txtPassengerCNIC.getText().trim(),  // CNIC
                txtPassengerContact.getText().trim(), // Contact
                flight.getFlightID(),               // Flight ID
                flight.getSource(),                 // Source
                flight.getDestination(),            // Destination
                flight.getDate(),                   // Date
                flight.getTime(),                   // Time
                seatClass,                          // Class
                seatNo,                             // Seat
                lblPrice.getText()                  // Price
        };

        PaymentPage paymentPage = new PaymentPage(ticketData);
        paymentPage.setVisible(true);

        this.dispose();

        // rest of your booking logic...
    }
