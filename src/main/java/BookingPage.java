    private void populateFlightDetails() {
        lblSelectedFlightID.setText(flight.getFlightID());
        cmbClass.removeAllItems(); // clear previous items

        if (flight.getFirstSeats() > 0) cmbClass.addItem("First");
        if (flight.getBusinessSeats() > 0) cmbClass.addItem("Business");
        if (flight.getEconomySeats() > 0) cmbClass.addItem("Economy");

        // Set first item as selected
        if (cmbClass.getItemCount() > 0) cmbClass.setSelectedIndex(0);

        updatePriceLabel();
    }

